def bin2hex(v):
    return hex(int(v.replace("|","").replace(" ",""),2)).upper().replace("X","x")

if __name__ == "__main__":
    bins = ["00 00 0 01 10100 01000 000 10000 0101011", "00 00 0 00 10 100 01000 000 10000 0101011"]
    for b in bins:
            h = bin2hex(b)
            print(f"{h}")

